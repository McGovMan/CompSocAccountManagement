public class Member {
    String type;
    String id;
    String fname;
    String lname;
    String email;
    String number;

    public Member(String type, String id, String fname, String lname, String email, String number){
        this.type = type;
        this.id = id;
        this.fname = fname;
        this.lname = lname;
        this.email = email;
        this.number = number;
    }
}
